import math # นำเข้าโมดูล math เพื่อใช้คำสั่งปัดเศษขึ้น

print("=== 🏠 โปรแกรมคำนวณงบปูกระเบื้อง ===")

# 1. รับขนาดห้องจากผู้ใช้
กว้าง = float(input("ความกว้างห้อง (เมตร): "))
ยาว = float(input("ความยาวห้อง (เมตร): "))

# คำนวณพื้นที่ (กว้าง * ยาว ที่คุณเคยเขียนไว้)
พื้นที่ = กว้าง * ยาว
print(f"\n📏 พื้นที่ห้องของคุณคือ {พื้นที่:.2f} ตารางเมตร")

# 2. รับข้อมูลกระเบื้องที่อยากได้
พื้นที่ต่อกล่อง = float(input("\nกระเบื้อง 1 กล่อง ปูได้กี่ตารางเมตร?: "))
ราคาต่อกล่อง = float(input("ราคากระเบื้องกล่องละกี่บาท?: "))

# 3. คำนวณจำนวนกล่องและราคา
# ใช้ math.ceil() เพื่อปัดเศษขึ้นเสมอ เช่น คำนวณได้ 4.2 กล่อง ก็ต้องซื้อ 5 กล่อง
จำนวนกล่อง = math.ceil(พื้นที่ / พื้นที่ต่อกล่อง) 
ราคารวม = จำนวนกล่อง * ราคาต่อกล่อง

# 4. สรุปผลลัพธ์
print("\n=== 📋 สรุปรายการเตรียมไปซื้อของ ===")
print(f"📦 ต้องใช้กระเบื้องทั้งหมด: {จำนวนกล่อง} กล่อง")
# ใช้ ,.2f เพื่อใส่ลูกน้ำหลักพันและทศนิยม 2 ตำแหน่งให้ดูเหมือนสกุลเงิน
print(f"💰 รวมเป็นเงินงบประมาณ: {ราคารวม:,.2f} บาท") 
print("====================================")
print("💡 คำแนะนำ: ในชีวิตจริงควรซื้อเผื่อไว้ 1-2 กล่อง เผื่อช่างตัดพลาดหรือกระเบื้องแตกนะครับ")